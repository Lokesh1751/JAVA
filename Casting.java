public class Casting {
    public static void main(String[] args){
        // Implicit Casting
        double price=100.00;
        double finalprice=price+18;
        System.out.println(finalprice);

        // Explicit Casting
        int p=100;
        // Convert 
        int f=p+ (int)18.0; 
        System.out.println(f);
    }
}
