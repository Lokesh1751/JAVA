import java.util.Scanner;

public class Loops {
    public static void main(String[] args){
        //for
        for(int i=0;i<5;i++){
            System.out.println(i);
        }

       // while
        int j=1;
        while(j<=10){
            System.out.println(j);
           j= j+1;
        }

       // do while
        int k=100;
        do{
            System.out.println(k);
            k=k-1;
        }
        while(k>=100);

        
        


    }
}
